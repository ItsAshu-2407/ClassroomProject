package Simple.studentRegister;

	public class studentInfo {
		int studentId;						//Variables Declared here. 
		String studentName;
		int studentAge;  
		int studentMarks;
		
		public studentInfo(int studentId, String studentName, int studentAge, int studentMarks) {
			this.studentId = studentId;                    //Storing Students Information.
			this.studentName = studentName;
			this.studentAge = studentAge;
			this.studentMarks = studentMarks;
		}
		
}

